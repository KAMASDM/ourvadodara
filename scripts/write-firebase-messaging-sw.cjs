const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..');

function readDotEnv() {
  const envPath = path.join(rootDir, '.env');
  if (!fs.existsSync(envPath)) return {};

  return fs.readFileSync(envPath, 'utf8')
    .split(/\r?\n/)
    .reduce((values, line) => {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith('#')) return values;

      const separatorIndex = trimmed.indexOf('=');
      if (separatorIndex === -1) return values;

      const key = trimmed.slice(0, separatorIndex).trim();
      let value = trimmed.slice(separatorIndex + 1).trim();
      value = value.replace(/^['"]|['"]$/g, '');
      values[key] = value;
      return values;
    }, {});
}

const filePath = process.argv[2]
  ? path.resolve(rootDir, process.argv[2])
  : path.join(rootDir, 'dist', 'firebase-messaging-sw.js');

const dotEnv = readDotEnv();
const env = { ...dotEnv, ...process.env };

const requiredKeys = [
  'VITE_FIREBASE_API_KEY',
  'VITE_FIREBASE_AUTH_DOMAIN',
  'VITE_FIREBASE_PROJECT_ID',
  'VITE_FIREBASE_STORAGE_BUCKET',
  'VITE_FIREBASE_MESSAGING_SENDER_ID',
  'VITE_FIREBASE_APP_ID',
];

const missing = requiredKeys.filter((key) => !env[key]);
if (missing.length > 0) {
  console.warn(`Skipping firebase-messaging-sw.js generation. Missing: ${missing.join(', ')}`);
  process.exit(0);
}

const firebaseConfig = {
  apiKey: env.VITE_FIREBASE_API_KEY,
  authDomain: env.VITE_FIREBASE_AUTH_DOMAIN,
  projectId: env.VITE_FIREBASE_PROJECT_ID,
  storageBucket: env.VITE_FIREBASE_STORAGE_BUCKET,
  messagingSenderId: env.VITE_FIREBASE_MESSAGING_SENDER_ID,
  appId: env.VITE_FIREBASE_APP_ID,
};

if (env.VITE_FIREBASE_MEASUREMENT_ID) {
  firebaseConfig.measurementId = env.VITE_FIREBASE_MEASUREMENT_ID;
}

const vapidKey = env.VITE_FIREBASE_VAPID_KEY || '';

const worker = `// Generated by scripts/write-firebase-messaging-sw.cjs
/* global firebase */

importScripts('https://www.gstatic.com/firebasejs/10.12.2/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/10.12.2/firebase-messaging-compat.js');

const firebaseConfig = ${JSON.stringify(firebaseConfig, null, 2)};
const vapidKey = ${JSON.stringify(vapidKey)};

firebase.initializeApp(firebaseConfig);
const messaging = firebase.messaging();

// Strip any HTML/entities so rich-text article bodies never render as tags.
function stripTags(value) {
  return String(value || '')
    .replace(/<[^>]*>/g, ' ')
    .replace(/&nbsp;/gi, ' ')
    .replace(/&amp;/gi, '&')
    .replace(/&lt;/gi, '<')
    .replace(/&gt;/gi, '>')
    .replace(/&#39;/gi, "'")
    .replace(/&quot;/gi, '"')
    .replace(/\\s+/g, ' ')
    .trim();
}

// Build a rich, branded notification from the FCM payload.
function buildNotification(payload) {
  const data = payload.data || {};
  const notification = payload.notification || {};

  const isBreaking = data.isBreaking === 'true';
  const title = stripTags(data.title || notification.title || 'Our Vadodara');
  const rawBody = stripTags(data.body || notification.body || '');
  const categoryLabel = stripTags(data.categoryLabel || data.category || '');

  // Lead the body with a context label so the card reads like a news alert.
  const label = isBreaking
    ? '🔴 BREAKING'
    : (categoryLabel ? categoryLabel.toUpperCase() : '');
  const bodyText = rawBody || 'Tap to read the full story.';
  const body = label ? label + '  •  ' + bodyText : bodyText;

  const image = data.image || notification.image || '';

  const options = {
    body,
    icon: '/icons/icon-192x192.png',
    badge: '/icons/icon-72x72.png',
    tag: data.tag || ('news-' + (data.postId || Date.now())),
    renotify: true,
    requireInteraction: isBreaking,
    vibrate: isBreaking ? [200, 100, 200, 100, 200] : [200, 100, 200],
    timestamp: Date.now(),
    data: {
      url: data.url || (data.postId ? '/post/' + data.postId : '/'),
      postId: data.postId || '',
      type: data.type || 'news',
      timestamp: Date.now(),
    },
    actions: [
      { action: 'view', title: 'Read Now' },
      { action: 'dismiss', title: 'Dismiss' },
    ],
  };

  // The large hero image (supported on Chrome/Android; ignored elsewhere).
  if (image) {
    options.image = image;
  }

  return { title, options };
}

messaging.onBackgroundMessage((payload) => {
  const { title, options } = buildNotification(payload);
  self.registration.showNotification(title, options);
});

self.addEventListener('notificationclick', (event) => {
  event.notification.close();

  if (event.action === 'dismiss') {
    return;
  }

  const data = event.notification.data || {};
  const targetUrl = data.postId ? \`/post/\${data.postId}\` : (data.url || '/');

  event.waitUntil(
    clients.matchAll({ type: 'window', includeUncontrolled: true }).then((clientList) => {
      for (const client of clientList) {
        if (client.url.includes(self.location.origin) && 'focus' in client) {
          client.focus();
          client.postMessage({
            type: 'NOTIFICATION_CLICK',
            url: targetUrl,
            data,
          });
          return;
        }
      }

      if (clients.openWindow) {
        return clients.openWindow(targetUrl);
      }
    })
  );
});

self.addEventListener('pushsubscriptionchange', (event) => {
  if (!vapidKey) return;

  event.waitUntil(
    self.registration.pushManager.subscribe({
      userVisibleOnly: true,
      applicationServerKey: vapidKey,
    })
  );
});
`;

fs.mkdirSync(path.dirname(filePath), { recursive: true });
fs.writeFileSync(filePath, worker);
console.log(`Wrote ${path.relative(rootDir, filePath)}`);
